export function getProductContent(product) {
  if (!product) return null;
  const categorySlug = product.category?.slug;
  const categoryContent = PRODUCT_CONTENT_BY_CATEGORY[categorySlug] || {};
  const override = PRODUCT_OVERRIDES_BY_SLUG[product.slug] || {};
  const notes = [...(categoryContent.notes || []), ...(override.notes || [])];

  return {
    ...categoryContent,
    ...override,
    notes,
    distributor: SITE_INFO.companyName,
    address: SITE_INFO.address,
  };
}

export function enhanceProduct(product) {
  if (!product) return product;
  const content = getProductContent(product);
  if (!content) return product;

  return {
    ...product,
    name: content.name || product.name,
    price: content.price || product.price,
    marketing: content,
  };
}
